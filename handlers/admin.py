async def broadcast_send(update: Update,
                         context: ContextTypes.DEFAULT_TYPE,
                         message_text: str = None):
    """–û—Ç–ø—Ä–∞–≤–∫–∞ —Ä–∞—Å—Å—ã–ª–∫–∏ (–≤—ã–∑—ã–≤–∞–µ—Ç—Å—è –∏–∑ —Ä–µ–∂–∏–º–∞ —Ä–∞—Å—Å—ã–ª–∫–∏)"""
    try:
        if not is_user_admin(update.effective_user.id):
            return

        if not message_text:
            if update.message and update.message.text:
                message_text = update.message.text
            else:
                await update.message.reply_text("‚ùå –ù–µ—Ç —Ç–µ–∫—Å—Ç–∞ –¥–ª—è —Ä–∞—Å—Å—ã–ª–∫–∏.")
                return

        # –°–±—Ä–∞—Å—ã–≤–∞–µ–º —Ä–µ–∂–∏–º —Ä–∞—Å—Å—ã–ª–∫–∏
        if 'broadcast_mode' in context.user_data:
            context.user_data.pop('broadcast_mode')

        # –ó–¥–µ—Å—å –±—É–¥–µ—Ç —Ä–µ–∞–ª—å–Ω–∞—è –æ—Ç–ø—Ä–∞–≤–∫–∞ –≤—Å–µ–º –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è–º
        # –ü–æ–∫–∞ –ø—Ä–æ—Å—Ç–æ —É–≤–µ–¥–æ–º–ª—è–µ–º –∞–¥–º–∏–Ω–∏—Å—Ç—Ä–∞—Ç–æ—Ä–∞
        await update.message.reply_text(
            f"‚úÖ **–ì–æ—Ç–æ–≤–æ –∫ —Ä–∞—Å—Å—ã–ª–∫–µ:**\n\n{message_text}\n\n"
            f"*–í —Ä–∞–∑—Ä–∞–±–æ—Ç–∫–µ:*\n"
            f"–í –±—É–¥—É—â–µ–º –∑–¥–µ—Å—å –±—É–¥–µ—Ç –æ—Ç–ø—Ä–∞–≤–∫–∞ {0} –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è–º.",
            parse_mode="Markdown")

        logger.info(
            f"–ê–¥–º–∏–Ω–∏—Å—Ç—Ä–∞—Ç–æ—Ä {update.effective_user.id} –ø–æ–¥–≥–æ—Ç–æ–≤–∏–ª —Ä–∞—Å—Å—ã–ª–∫—É: {message_text[:50]}..."
        )

    except Exception as e:
        logger.error(f"–û—à–∏–±–∫–∞ –≤ broadcast_send: {e}")
        await update.message.reply_text("‚ùå –û—à–∏–±–∫–∞ –ø—Ä–∏ –ø–æ–¥–≥–æ—Ç–æ–≤–∫–µ —Ä–∞—Å—Å—ã–ª–∫–∏.")


